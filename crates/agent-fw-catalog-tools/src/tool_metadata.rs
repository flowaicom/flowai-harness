pub const GET_CATALOG_RELATIONS: CatalogToolMetadata = CatalogToolMetadata {
    name: "get_catalog_relations",
    description: concat!(
        "/**\n",
        " * Fetch adjacent catalog graph relations for selected refs.\n",
        " *\n",
        " * Use this when:\n",
        " * - You have selected entity refs and need neighboring tables, columns, enum values, metrics, knowledge, or relationship vertices.\n",
        " * - You need to understand semantic-rule scope, metric dependencies, enum values for a column, or direct relationship context.\n",
        " * - Missing information is catalog context, not target database rows.\n",
        " *\n",
        " * Do not use this when:\n",
        " * - You need initial discovery from a phrase; use search_catalog first.\n",
        " * - You need full paths between two known endpoints; use get_relation_paths_between.\n",
        " * - You need table fields only; use list_schema_fields.\n",
        " * - You are ready to run SQL with complete context; use execute_query.\n",
        " *\n",
        " * Inputs:\n",
        " * - refs: Required array of 1..=20 CatalogRef objects whose adjacent graph relations should be returned.\n",
        " * - direction: Optional relation direction relative to each input ref: `outgoing`, `incoming`, or `both`. Use `both` for context discovery.\n",
        " * - relation_kinds: Optional relation kind strings such as `has_column`, `references_table`, `enum_value_of`, `applies_to`, `metric_uses`, `synonym_of`, or `equivalent_to`.\n",
        " * - target_kinds: Optional array of target entity kinds to return.\n",
        " * - limit_per_ref: Optional requested related-entity count per ref, 1..=100.\n",
        " *\n",
        " * Behavior:\n",
        " * - Resolves each source ref exactly, traverses catalog links/relations in the requested direction, hydrates targets, and applies relation/target filters.\n",
        " * - Does not execute SQL, sample target rows, or mutate catalog state.\n",
        " *\n",
        " * Returns:\n",
        " * - results: One result per source with the source entity, adjacent relation edges, target entities, pagination, and warnings.\n",
        " * - relation edges include relation_kind, direction, target, optional description, and optional relationship vertex details.\n",
        " * - warnings: Non-fatal missing-target, truncation, or filter issues.\n",
        " *\n",
        " * After calling:\n",
        " * - Use adjacent targets to choose the next hydration or schema-inspection step.\n",
        " * - For SQL joins between endpoints, call get_relation_paths_between or verify join fields before execute_query.\n",
        " */"
    ),
};

pub const GET_RELATION_PATHS_BETWEEN: CatalogToolMetadata = CatalogToolMetadata {
    name: "get_relation_paths_between",
    description: concat!(
        "/**\n",
        " * Find join or semantic graph paths between selected catalog refs.\n",
        " *\n",
        " * Use this when:\n",
        " * - You already have source and target refs and need to connect them for SQL planning or semantic explanation.\n",
        " * - You need join hops between tables or query surfaces before writing SQL.\n",
        " * - You need to trace how knowledge, metrics, or semantic rules reach concrete tables and columns.\n",
        " *\n",
        " * Do not use this when:\n",
        " * - You have not discovered endpoints yet; use search_catalog first.\n",
        " * - You only need adjacent one-hop context; use get_catalog_relations.\n",
        " * - You only need fields for one table; use list_schema_fields.\n",
        " *\n",
        " * Inputs:\n",
        " * - from: Required CatalogRef for the source endpoint.\n",
        " * - to: Required array of 1..=20 CatalogRef target endpoints.\n",
        " * - path_type: Optional path family: `join` for SQL join paths, `semantic` for graph/semantic context, or `any` to try both.\n",
        " * - max_depth: Optional maximum semantic graph depth, 1..=6.\n",
        " *\n",
        " * Behavior:\n",
        " * - Resolves endpoints exactly, then searches catalog join metadata and/or semantic graph edges within the requested depth.\n",
        " * - For `any`, attempts join paths first and falls back to semantic paths when no join path is found.\n",
        " * - Does not execute SQL, sample rows, or mutate state.\n",
        " *\n",
        " * Returns:\n",
        " * - paths: One path result per target with from_entity, to, found, path_type, steps, length, and warnings.\n",
        " * - steps may include via_relation details and join metadata such as from_column, to_column, and join_type.\n",
        " * - found=false means no path was found within limits; warnings may indicate truncation or unresolved endpoints.\n",
        " *\n",
        " * After calling:\n",
        " * - Use found join steps to build SQL joins, then verify required fields with list_schema_fields before execute_query.\n",
        " * - If no path is found, do not invent joins; search or hydrate additional refs instead.\n",
        " */"
    ),
};

