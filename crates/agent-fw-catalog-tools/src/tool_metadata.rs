pub const EXECUTE_QUERY: CatalogToolMetadata = CatalogToolMetadata {
    name: "execute_query",
    description: concat!(
        "/**\n",
        " * Run a validated read-only SQL query as the final step.\n",
        " *\n",
        " * Use this when:\n",
        " * - You have already completed catalog discovery and targeted hydration.\n",
        " * - You have finished confirming tables, fields, joins, filters, and semantic rules with catalog tools.\n",
        " * - The user needs actual target database rows, aggregates, or final analysis.\n",
        " *\n",
        " * Do not use this when:\n",
        " * - You only have broad search_catalog candidates or uncertain table/field choices.\n",
        " * - You still need schema details; use list_schema_fields.\n",
        " * - You still need joins, relationship context, metrics, or semantic rules; use get_catalog_entities, get_catalog_relations, or get_relation_paths_between.\n",
        " * - You need representative examples but not final analysis; use sample_table_data.\n",
        " *\n",
        " * Inputs:\n",
        " * - sql: Required single read-only SELECT or WITH query. Mutations, DDL, multi-statement SQL, and unsafe clauses are rejected. Use catalog-confirmed table and column names.\n",
        " * - params: Optional positional bind parameters for user-provided values where possible.\n",
        " * - limit: Optional requested output row limit, 1..=1000. The runtime enforces or wraps limits and may clamp large requests.\n",
        " * - purpose: Optional short audit/trace explanation of why this query is being run.\n",
        " *\n",
        " * Behavior:\n",
        " * - Parses and validates SQL as read-only SELECT/WITH, rejects mutations and DDL, applies/enforces row limits, and executes against the target database.\n",
        " * - Reads target database data. Does not mutate target rows or catalog state.\n",
        " *\n",
        " * Returns:\n",
        " * - columns: Output column names.\n",
        " * - rows: Returned row objects.\n",
        " * - row_count: Number of rows returned.\n",
        " * - truncated: Whether the runtime stopped before all possible rows because of limits.\n",
        " * - warnings: Non-fatal validation, limit, or execution notes.\n",
        " *\n",
        " * After calling:\n",
        " * - Use returned rows to answer the user, noting truncation or warnings.\n",
        " * - If the query fails due to missing context, return to catalog hydration/schema/relation tools rather than guessing names or joins.\n",
        " */"
    ),
};

