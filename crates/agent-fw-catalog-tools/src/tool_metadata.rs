//! Shared tool names and descriptions for catalog tool adapters.
//!
//! Agent-facing descriptions live here so runtime prompt rendering and
//! `ToolHandler::definition()` cannot drift.

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct CatalogToolMetadata {
    pub name: &'static str,
    pub description: &'static str,
}

pub const SEARCH_CATALOG: CatalogToolMetadata = CatalogToolMetadata {
    name: "search_catalog",
    description: concat!(
        "/**\n",
        " * Discover candidate catalog entities from search text.\n",
        " *\n",
        " * Use this when:\n",
        " * - You need to find relevant tables, columns, enum values, metrics, knowledge, documents, or relationships from a phrase such as `table_name`, `column_name`, `metric_name`, or `schema.table_name`.\n",
        " * - You are starting a catalog workflow and do not yet know the exact catalog ids or qualified names to inspect.\n",
        " * - You need facets or suggested filters to narrow a broad domain term before hydrating details.\n",
        " *\n",
        " * Do not use this when:\n",
        " * - You already have selected ids or qualified names and need canonical details; use get_catalog_entities.\n",
        " * - You need table columns, data types, keys, or profiling summaries; use list_schema_fields after selecting table refs.\n",
        " * - You need joins, adjacent graph context, or semantic-rule scope; use get_catalog_relations or get_relation_paths_between after selecting refs.\n",
        " * - You are ready to run SQL; use execute_query only after targeted hydration confirms the query context.\n",
        " *\n",
        " * Inputs:\n",
        " * - query: Natural-language search phrase or identifier. Required string. Examples: `table_name`, `column_name`, `metric_name`, `schema.table_name`, `literal_value`.\n",
        " * - kinds: Optional array of entity kinds to search. Values include `table`, `column`, `relationship`, `enum_value`, `metric`, `knowledge`, `document`, and `data_quality_finding`. Omit to search all public kinds.\n",
        " * - filters: Optional structured filters applied during search. Supports database_id, schema, table, column, data_type, semantic_type, tags, knowledge_type, relation/source/target fields, preferred_query_surface, and low_cardinality_enum.\n",
        " * - limit: Optional requested result count, 1..=50. The runtime may clamp broad requests lower.\n",
        " * - cursor: Optional opaque cursor from a prior search_catalog call with the same logical query.\n",
        " *\n",
        " * Behavior:\n",
        " * - Runs lexical catalog search through the runtime catalog search backend in the current tenant/workspace scope.\n",
        " * - Hydrates candidate ids internally only to drop stale hits and shape compact result envelopes.\n",
        " * - Does not mutate catalog data, target database rows, or workspace state.\n",
        " * - Does not return complete schema, joins, or executable query context.\n",
        " *\n",
        " * Returns:\n",
        " * - query: Normalized query string used for search.\n",
        " * - results: Ranked candidate CatalogEntity values with ids, names, compact details, tags, match diagnostics, and warnings. Treat these as candidates, not final query context.\n",
        " * - facets and suggested_filters: Counts and filter hints for narrowing follow-up discovery.\n",
        " * - filter_resolution: Canonical applied filters plus unresolved or ambiguous filters.\n",
        " * - pagination: returned, limit, has_more, and optional next_cursor.\n",
        " * - diagnostics: Runtime search details such as backend, candidate_count, hydrated_count, and round_trips. Do not branch on backend names.\n",
        " *\n",
        " * After calling:\n",
        " * - Select only the relevant refs, then call get_catalog_entities for canonical details, list_schema_fields for table columns, or relation/path tools for joins and semantic-rule context.\n",
        " * - Do not treat broad search results as executable SQL context; hydrate selected refs and verify fields, joins, filters, and semantic rules before execute_query.\n",
        " */"
    ),
};

pub const GET_CATALOG_ENTITIES: CatalogToolMetadata = CatalogToolMetadata {
    name: "get_catalog_entities",
    description: concat!(
        "/**\n",
        " * Hydrate selected catalog refs into canonical typed entities.\n",
        " *\n",
        " * Use this when:\n",
        " * - Use after search_catalog with selected ids or qualified names when you need authoritative typed details.\n",
        " * - You need provenance, relation refs, metric definitions, knowledge/document body context, enum metadata, or data-quality details for specific entities.\n",
        " * - You already narrowed broad discovery and want the next level of detail for a small, explicit ref set.\n",
        " *\n",
        " * Do not use this when:\n",
        " * - You need broad discovery from a phrase; use search_catalog first.\n",
        " * - You need table column lists or field profiles; use list_schema_fields for table columns.\n",
        " * - You need graph adjacency or join paths; use get_catalog_relations or get_relation_paths_between.\n",
        " *\n",
        " * Inputs:\n",
        " * - refs: Required array of 1..=50 CatalogRef objects. Prefer `id` values returned by prior catalog tools. Use `qualified_name` such as `schema.table_name` when ids are not known. Use `name` with `kind` only when necessary to disambiguate.\n",
        " *\n",
        " * Behavior:\n",
        " * - Resolves refs exactly through the scoped catalog, preserving requested order where possible.\n",
        " * - Batch-hydrates catalog entries and assembles runtime-selected entity details.\n",
        " * - May hydrate document or knowledge body text from the configured KV payload store.\n",
        " * - Does not search broadly, inspect all table fields, execute SQL, or mutate state.\n",
        " *\n",
        " * Returns:\n",
        " * - entities: Hydrated CatalogEntity values. Small ref sets may use verbose details; larger sets may use summary details.\n",
        " * - missing: Input refs that could not be resolved or disappeared before hydration.\n",
        " * - warnings: Non-fatal metadata, ambiguity, or payload hydration issues.\n",
        " *\n",
        " * After calling:\n",
        " * - Use list_schema_fields for selected table/query-surface columns, get_catalog_relations for adjacent context, or get_relation_paths_between for joins before execute_query.\n",
        " * - Treat missing or warning entries as uncertainty to resolve before SQL planning.\n",
        " */"
    ),
};

